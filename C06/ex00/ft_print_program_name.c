#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int	main(int count, char **argc)
{
	if (count == 1)
		ft_putstr(argc[0]);
	return (0);
}
